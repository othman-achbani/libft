#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	d;
	int	r;

	i = 0;
	d = 1;
	r = 0;
	while ((str[i] >= '\t' && str[i] <= '\r' ) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			d = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - '0');
		i++;
	}
	return (r * d);
}
